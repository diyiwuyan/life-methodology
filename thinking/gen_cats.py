def build_nav(current_num, rgb):
    links = []
    for n, icon, label in ALL_CATS:
        cls = ' class="active"' if n == current_num else ''
        links.append(f'    <a href="cat-{n}.html"{cls}>{icon} {label}</a>')
    return "\n".join(links)

def build_cards(models, rgb):
    parts = []
    for num, title, tag, desc, steps in models:
        steps_html = []
        for i, s in enumerate(steps, 1):
            if "：" in s:
                key, val = s.split("：", 1)
                steps_html.append(
                    f'        <div class="step"><div class="step-dot">{i}</div>'
                    f'<div class="step-text"><strong>{key}</strong>{val}</div></div>'
                )
            else:
                steps_html.append(
                    f'        <div class="step"><div class="step-dot">{i}</div>'
                    f'<div class="step-text">{s}</div></div>'
                )
        steps_block = "\n".join(steps_html)
        parts.append(f"""  <div class="model-card" id="m{num}">
    <div class="model-header" onclick="toggle('m{num}')">
      <span class="model-num">{num}</span>
      <span class="model-title">{title}</span>
      <span class="model-tag">{tag}</span>
      <span class="model-arrow">▼</span>
    </div>
    <div class="model-body">
      <p class="model-desc">{desc}</p>
      <div class="model-steps">
{steps_block}
      </div>
    </div>
  </div>""")
    return "\n\n".join(parts)

